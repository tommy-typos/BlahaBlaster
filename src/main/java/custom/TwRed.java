package custom;

import java.awt.*;

/**
 * TwRed class provides a palette of predefined red colors. These colors are defined as public
 * static final fields.
 */
class TwRed {
  /** Dark red color with a shade value of 700 */
  public static final Color _700 = new Color(185, 28, 28);

  /** Darker red color with a shade value of 900 */
  public static final Color _900 = new Color(127, 29, 29);
}
